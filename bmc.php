                <h2>BMC – model</h2>
            
            <img src="bmc.png" alt="bmc" width="70%">
                
                <p>En forretningsmodel er vigtig i forhold til at kunne forstå virksomheden man arbejder med, eller sin egen virksomhed. Hvordan de engagerer sig med sine kunder, kundefordele, ressourcer, nøglepartnere mm. Det er et strategisk ledelsesværktøj, der kan anvendes i forhold til strategisk anvendelse, planlægning og udvikle virksomhedens forretningsmodel.</p>
                
                <h3>De 7 arketyper</h3>
            
               <dl>
                    <dt>1. Den digitale købmand</dt>
                    <dd>Har sin fysiske butik men supplerer med salg via Internettet.<br>
                        Branding, annoncering, varekataloger, service- og vejledninger på nettet.</dd>
                    <dt>2. Webshoppen</dt>
                    <dd>Køber hos grosister og sælger direkte til forbrugere via internettet.<br>Undgår fordyrende fysisk detailbutik omkostninger og har samtidigt større varesortiment.</dd>
                    <dt>3. Den digitale producent</dt>
                    <dd>Udvider sine fysiske produkter med digitale tjenester bl.a. med Internet Of Things.<br>Skaber nye funktionaliteter samt digitale lock-in effekter om det fysiske produkt.</dd>
                    <dt>4. Software producenten</dt>
                    <dd>Bygger software med licens/direkte betaling.<br>Marginalomkostninger grænsende til nul, skalerbarhed og udrulning/opdatering via internet.</dd>
                    <dt>5. Den digitale platformsbygger</dt>
                    <dd>Bygger sin egen digitale platform på internettet. <br>Skalering af antal bruger og brugen kilden til indirekte salg.</dd>
                   <dt>6. App opfinderen</dt>
                   <dd>Ser et behov og bygger en app som dækker behovet.<br>Ekstremt lave start-up og exit costs.</dd>
                   <dt>7. Wiki-skaberen</dt>
                   <dd>Brugerne er både skabere samt forbrugere af indholdet.<br>Indtjening sker gennem donationer og eller indirekte salg.</dd>
            </dl>
            
