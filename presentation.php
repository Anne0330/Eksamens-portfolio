        <h2>Kropssprog</h2>
            <p>Non-verbal kommunikation, er alt fra kropssprog til, tøjstil, ansigtsudtryk og adfærden. Det er noget kroppen gør af natur og tit bevidst, alt efter hvad man siger. Alle laver observationer der ikke hænger sammen i forhold til det verbale, med hvilket tøj personen har på, hvordan siger det talen, hvordan er posituren osv.<br>
            Kropssproget giver en form for troværdighed og gør det lettere at sælge sit stof, hvis man kan styre og vise det. Passer kropssproget med det verbale, er man ligeledes på vej til at få et ’forhold’ mellem personerne. Det er vigtigt og vide hvem modtageren er, hvilken kultur har de eller andre holdninger man burde tage hensyn til.</p>
