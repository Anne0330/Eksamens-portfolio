        <h2>Fisken</h2>
            <img src="fisken.jpg" alt="fisken" width="70%">
                <dl>
                    <dt>Begyndelsen/Hovedet</dt>
                    <dd>Det er indledningen, som er vigtig i forhold til at fange modtagernes interesse. Det kan være stikord eller en intro med punkter, der forklarer hvilke emner du vil gå igennem, i løbet af præsentationen. Det er et førstehåndsindtryk, hvilket gør at man skal være observant på hvordan man starte sit oplæg. Vær konkret, selvsikker og vis dem hvem du er.</dd>
                    <dt>Midten/Kroppen</dt>
                    <dd>Selve fremlæggelsen, som bliver eksponeret til modtagerne. Det er her, man skal vise alt den data man har fået, eller viden man skal forklare som man har opdaget undervejs i sin research. Kom med gode pointer, analyse, historie og eventuelle sjove fakta. Inkluder dine modtagere og få dem til at føle de er en del af det, ved at give dem chance for spørgsmål til sidst.</dd>
                    <dt>Slutningen/Halen</dt>
                    <dd>Gentag dine pointer fra talen, for at få dem til at huske det og komme med en afrunding af hele præsentationen. Eventuelt inden alt slutter, så sørg for at inkludere alle der har spørgsmål til en dialog mellem modtager og afsender. </dd>
