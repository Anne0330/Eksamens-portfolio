        <h2>Design thinking i praktisk</h2>
          
            <p>Dette projekt var en tilgang med hentydning at bruge design thinking i praktisk, hvor der var krav på at få lavet en prototype på en app. Vi havde et overemne der hed ”Udlån og reservation”, som var tildelt i grupper. Med Magnus, Christine og Heidi Maria som min studiegruppe, gik vores design thinking forløb således.</p>
            
        <h3>Fase 1. Empathize og 2. Define</h3>
            
            <p>Vi startede ud med en brainstorm på tavlen, ved at have ”udlån og reservation” som hovedemne. Vi brugte et par timer på at skrive diverse emner vi kunne komme på, der havde en forbindelse med vores emne. Det største problem vi skulle lære at håndtere, var at ikke og gå direkte til ideate fasen. Efter vi havde brugt en dag med at skrive emner ned, begyndte vi at indsnævre på et under emne som vi kunne have interesse i. Vi endte med at gå med ”lån fra naboen”, til at tænke på overskud af aftensmad som man fik i en god stund. Derfra var vi interesseret i emnet ”madspild”, da det havde meget ’kød’ på sig til at vi følte vi kunne få meget data og viden.</p>
            
                <img src="brainstormreservation.jpg" alt="brainstorm" width="70%">
            
            <p>Vi tog en kvalitativ tilgang ved at gå ud og interviewe almindelige borgere, supermarkeder og skraldere. Efter vi havde fået den data vi var tilfredse med, kunne vi udarbejde en problemstilling. Vi havde en problemstilling der lød på, at det skulle være nemmere at kunne give sin madrester til naboer eller andre der kunne have god brug af det. Det viste sig så, er der var flere problemer end vi kunne løse, men besluttede os for at tage en portion vi kunne gøre en forskel på.</p>
            
                <img src="brainstormreservation2.jpg" alt="brain storm" width="70%">
            
        <h3>Fase 3. Ideate, 4. Prototype og 5. Test.</h3>
            
            <p>Derfra kunne vi smide alle idéerne vi havde parkeret fra fase 1, kunne vi tildele endnu en tavle med vores idéer. Vi vidste godt fra starten at det skulle være en app, så vi havde en ramme at arbejde indenfor. Derfra arbejde vi videre med idéen at det skulle virke som en form for ”tinder” app med mad, som ikke var virksomhedsdrevet. Derfra arbejde vi med noget design på en tavle, hvor vi således byggede vores prototype af papir og pap.</p>
            
                <img src="prototypesketch.jpg" alt="tavle sketch prototype" width="70%">
            
                <img src="prototype.jpg" alt="prototype" width="70%">
            
            <p>Der kunne vi også gøre god brug af ”journey mapping/bruger rejse”, som gør at man forestiller sig en kunde der skal bruge denne app og hvilken oplevelse han vil få. Derudover kan lave ’touch points’, hvilket betyder at man prøver og gøre en forskel for denne kunde fra hvad virksomheden/vi kan gøre. Her er vores bruger rejse som vi brugte til vores ide fase, for at skabe et design og funktion.</p>
            
                <img src="brugerrejsen.jpg" alt="brugerrejsen" width="70%">
            
            <p>Efter vi havde færdiggjort den første prototype, fik vi tre studerende til at teste vores prototype. Vi fik respons på at der skulle ændres nogle småting og der kunne være flere funktioner, imens vi havde tid til at rette dem.</p>
            
        <h3>Fase 6. Implementering</h3>
            
            <p>Ved afslutning af vores design thinking projekt, lavede vi en pitch/salgs tale som blev præsenteret foran resten af klassen. En video med hvordan appen fungerede, hvor vi således kunne svare på spørgsmål eller konstruktiv kritik til vores prototype.</p>
            
            <p>Jeg husker tydeligt at vi blev spurgt, om vi kunne finde på at videre arbejde med prototypen, så det kunne ende med at blive en reel app. Selv om proceduren med denne prototype blev til en god oplevelse, følte jeg ikke at den er var original nok til at jeg ville finde den frem for at producere den videre. Dog, kunne jeg godt finde på at finde den frem og ændre i design, eller funktionerne vi valgte i appen.</p>
        
