        <div id="design">
            
            <h3>Fase 1. Empathize / Dyb forståelse</h3>
            
                <p>Denne fase er hvor man gør sig nysgerrig og gør sig åben for ny viden, ved at skabe et overblik. Der stilles emner på en tavle i form af brainstorm, som man har kommet på eller tilset sig nysgerrighed på. Denne fase tager typisk en dag, eller efter hvor lang tid man har til at kunne være nysgerrig, men det ses at man ikke ’stopper’ med at være nysgerrig i hele processen. Der tilgås en kvalitativ tilgang med hentydning af indsamling til data, hvor man eventuelt kan afvise emnet eller parkerer den til senere. Det er tit et problem, at man kommer med ideer til løsning i denne fase, men det er ment til fase 3. Ideate. Derfor rådes det til at man parkerer sin ide, til at kunne fremvise den til ideate fasen.</p>
            
            <h3>Fase 2. Define / Definer problemet</h3>
            
                <p>I denne fase har man indsamlet data fra Empathize fasen, som man udgør nogle væsentlige ’problemer’. Der opstilles en problemstilling med hentydning til det valgte emne, eller der opstår et problem med selve emnet, ved der ikke er data nok. Det er betydeligt godkendt at hoppe tilbage i en fase, hvis der mangles viden mm.</p>
            
            <h3>Fase 3. Ideate / Idé fasen</h3>
            
                <p>Efter man har fundet problemet man ønsker at løse, er det tid til at komme med idéer. Dette er fasen som de fleste falder i først, når det gælder design thinking. Det er den kreative del af fasen hvor man tænker ud af boksen, for at kunne udfolde sine idéer med løsninger på problemet.</p>
            
            <h3>Fase 4. Prototype / Producer en prototype</h3>
                
                <p>I denne fase kommer din ide til livs, som bliver lavet ud af materialer som kan være papir. Det anses ikke at det skal være det færdige produkt, men fremstår som et skelet. Det gør det som regel nemmere, at man kan ændre i papir-prototype efter at få den testet.</p>
            
            <h3>Fase 5. Test / Bruger testen</h3>
                
                <p>I denne fase tager man prototypen ud til en målgruppe af test personer, for at få data ind. Dette gør at man kan notere og opleve hvordan en bruger, ville antage prototypen. Derfor kan der opstå problemer, mangel på viden eller nye ideer, når prototypen bliver testet. Ved fase 4 og 5, kan man ende med at hoppe frem og tilbage før man har det en prototype der løser problemet man har valgt at løse.</p>
            
            <h3>Fase 6. Implement / Pitchen</h3>
            
                <p>Dette er den ’endelige’ fase, hvor man har mulighed for at offentliggøre ideen til befolkning eller en investor. Derfor kan man ende med at lave en ’pitch’ som er en salgs-tale, for at fx denne investor skal tage ideen og producerer den videre til offentligheden. Det anses ikke at man er færdig med processen, da man altid tilgår sig ny viden og der kan opstå muligheder at lave en bedre version af prototypen eller et nyt problem. Derfor stopper design thinking processen aldrig, og vil såvel være en cyklus.</p>
            
            <h3>Bruger rejse</h3>
            
                <p>En bruger rejse, er at man iagttager eller forestiller sig en kunde der skal opleve dit produkt. Man noterer ned hvad de gør under hele processen, hvor der delvist kan have åbne tanker eller humør stumper. En øvelse vi skulle lave, var at forestille en kunde skulle en tur i vores biograf. Vi noterede hvad han gjorde, med nogle åbne tanker og samt havde humør stumper med. Efter at have lavet en tidslinje for vores bruger, kunne vi begynde og lave 'touch points'. Touch points er hvor virksomheden kan komme i spil, for at gøre oplevelsen bedre. Det betyder simpelthen, at virksomheden har været inde og 'røre'. I vores tilfælde, satte vi grønne lapper på hvor vi kunne lave eventuelle touch points. Der er ikke taget forbehold for at være kritisk, da vi intet vidste om budget eller lign.</p>
            
                    <img src="brugerjourney.jpg" alt="Journey map" width="70%">
            
            <h3>Divergent og konvergent tænkning</h3>
            
                <p>Der er nemt at være kreativ og nysgerrig, men det tildeles også at man skal være kritisk. Ved at man er nysgerrig og kreativ, ved at udfolde sig med ideer samt at lede videre til flere ideer for at udvide projektets ramme, bliver det kaldt at ”divergent tænkning”.<br>
                ”Kill your darlings”<br>
                Ved en konvergent tankegang betyder man at der ’skydes’ ideer ned som ikke er vigtige. Det handler om at være kritisk, hvor man indsnævrer projektets ramme igen.</p>
            
