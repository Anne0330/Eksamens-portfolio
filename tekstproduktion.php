            <h2>Genre</h2>
                <p>Udover at kende til brugeren eller læseren, er det vigtigt at kunne skrive den rigtige tekst til kanalen. Der er struktur, i forhold til hvor teksten er skrevet til. Hvilket afgør hvordan strukturen ser ud for teksten, og hvordan den skal skrives for at finde læseren.</p>
                
            <h3>Der er forskellige typer for medier/genre er:</h3>
                <dl> 
                    <dt>Artikel</dt>
                    <dd>En klassisk artikelstruktur, som man ser i avisen/magasinet.</dd>
                    <dt>Brochure</dt>
                    <dd>En informationsstruktur, som giver en form for information eller oplysninger.</dd>
                    <dt>Annonce</dt>
                    <dd>En traditionel opbygning.</dd>
                    <dt>Webtekster</dt>
                    <dd>Som regel en ’hurtig’ tekst til den ’travle læser’. Webtekster skrives som regel, ikke ligesom en tryktekst.</dd>
                    <dt>Sociale medietekster</dt>
                    <dd>Spontanitet og hurtig tekst. Den fanger en bestemt målgruppe.</dd>
                    
                </dl>
