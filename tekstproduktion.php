        <h3>Den gode tekst på nettet</h3>
            <dl>
                <dt>Hovedbudskab</dt>
                <dd>Konklusion skal skrives i overskriften, da det skal være hurtigt og læse.</dd>
                <dt>Let at skimme/baggrund</dt>
                <dd>Folk har som regel travlt og hvis det de leder efter, ikke er indenfor de første to linjer, skipper de videre. Teksten skal være nem, overskuelig og ”lige til”.</dd>
                <dt>Skrevet i lag</dt>
                <dd>Detaljerne for baggrund.</dd>
            </dl>
        
        <h3>De fire sprogdyder</h3>
        
            <dl>
                <dt>Aptum – hensigtsmæssighed = det passende sprog</dt>
                <dd>Betegner talens tilpasning til alle de forhold, der har betydning for, om afsenderen opnår sit mål med talen.<br>Emnet, modtager/målgruppe, modtagers forhåndsviden og indstilling til emnet.<br>”Adressatrelevans”; situationalitet og acceptabilitet.</dd>
                <dt>Puritas - Renhed = Korrekthed</dt>
                <dd>Puritas drejer sig om at overholde sprogets regler for korrekthed med hensyn til grammatik, udtale, stavning, syntaks osv.<br>Kan ‘oversættes’ til sprogrigtighed.</dd>
                <dt>Ornauts - Udsmykning = Giv teksten liv</dt>
                <dd>Sproglig udsmykning, der skal gøre sproget mere levende, personligt og vedkommende.<br>Ornauts skal ikke forstås blot som sproglig udsmykning.<br>En levende og overbevisende fremstilling skal anskueliggøre emnet, gøre det nærværende og personligt vedkommende.<br>Hvis sproget er korrekt og klart – og meget præcist, så kan det tit blive kedeligt.<br>Der skal pynt til: Metaforer, billeder, ordvalg, syntaks mm.</dd>
                <dt>Perspicuitas: Gennemsigtighed = Klarhed</dt>
                <dd>Tekstens sproglige klarhed og dens gennemskuelighed. Det er tekstens perspicuitas, der sikrer dens forståelighed.</dd>
            </dl>

