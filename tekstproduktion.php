            <h3>De fem nyhedskriterier</h3>
            
                <p>Man anvender de fem nyhedskriterier som en guide, som er med til at bestemme værdien for nyheden. Man anser at en nyhedsartikel opfylder 2-3 af nyhedskriterierne, da man kan få 3 ting ud af det som er, Kvalitet, delinger og links.</p>
            
                 <dl>
                    <dt>Væsentlighed</dt>
                    <dd>At noget har en grundlæggende betydning eller konsekvens for mange mennesker.<br> Ex: Skatten stiger næste år.</dd>
                    <dt>Sensation</dt>
                    <dd>Noget der fascinerer eller chokerer. Ofte de små, skæve historier i nogle aviser, som kan være forsidehistorier i andre aviser.<br>Ex: Mand bider hund eller Kvinde overlevede en måned under sammenstyrtet hus.</dd>
                    <dt>Konflikt</dt>
                    <dd>Når sager eller personer er på kollisionskurs. Ex: Vred far anklager kommune for sjusk.</dd>
                    <dt>Identifikation</dt>
                    <dd>Noget som læseren kan genkende sig selv i. En historie, der får læseren til at tænke ’Det kunne være mig’, ’Bare det var mig’ eller ’Godt, det ikke er mig’.<br>Ex: Ni ud af ti danskere spiser for meget sukker. </dd>
                    <dt>Aktualitet</dt>
                    <dd>Noget der er oppe i tiden. Historien må gerne udspringe af stof som i forvejen cirkulerer i andre medier. Langt de fleste nyheder udspringer af dette nyhedskriterium. Ex: Et parti vælger ny formand eller et containerskib er sejlet ind i en bro.</dd>
                </dl>
            
            <h4>AIDA modellen</h4>
            
                <img src="aida.png" alt="aida Model" width="70%">
            
                    <p>AIDA modellen bruges som regel til plakater, reklamationer, videoer og opslag for at få modtagerens opmærksomhed.</p>
            
                        <dl>
                            <dt>Attention</dt>
                            <dd>Positiv opmærksomhed<br>Relevans for modtager</dd>
                            <dt>Interest</dt>
                            <dd>Fastholde interessen for sagen/teksten, produkt eller budskab.</dd>
                            <dt>Desire</dt>
                            <dd>Skab et behov<br>Eventuelt bruger følelser.</dd>
                            <dt>Action</dt>
                            <dd>Hvad skal modtageren gøre, i forhold til teksten.<br>Eks. Købe produktet der er reklameret for</dd>
                        </dl>

                    <p>Der skal ligeledes skrives en tekst der passer til indholdet, og få opmærksomheden fra målgruppen. Derved kan man bruge følgende strategier, som kan blandes og passes ind i forhold til billeder, grafik og andre medier.</p>
            
                        <dl>
                            <dt>USP/Unique Selling Proposition</dt>
                            <dd>Denne bruges til tilbud der er ’lige nu og her’ eller kvalitet, udbud, sted og lignende.</dd>
                            <dt>ESP/Emotional Selling Proposition</dt>
                            <dd>Man forsøger at komme i kontakt med følelserne med en pathos strategi, der kan enten være godt eller ondt.</dd>
                            <dt>ISP/Ironic Selling Proposition</dt>
                            <dd>Man bruger ironi eller en humor der kan virke stødende, eks. Sort humor.</dd>
                            <dt>BSP/Brand Selling Proposition</dt>
                            <dd>Et mærke med høj rank i branchen, i forhold til det produkt der bliver solgt.</dd>
                            <dt>OSP/Orginizational Selling Proposition</dt>
                            <dd>Mærker som er værdifulde, vil betyde at produkter bliver solgt baseret på navnet har værdi.</dd>
