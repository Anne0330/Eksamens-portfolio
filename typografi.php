        <div class="klassifikation">
            <h3>Fontklassifikationer - De to vigtigste</h3>
            
                <dl>
                    <dt>SERIF / ANTIKVA</dt>
                    <dd>Har fået sit navn fra sine ‘fødder’ - den tynde linie som afslutter grundstregen på et bogstav eller et symbol. Benyttes ofte som brødtekst, da fødderne gør større mængder tekst letlæselige. Fødderne danner nærmest en linie for øjet at følge. Kan opfattes som gammeldags.</dd>
                    <dt>SANS SERIF / GROTESK</dt>
                    <dd>Sans er fransk og betyder ‘uden’ – altså ‘uden serif’ eller ‘uden fødder’. Benyttes ofte til overskrifter, da den stramme moderne stil giver en god kontrast til den letlæselige serif. Har i lang tid været det fortrukne som brødtekst (copy) på moderne websites.</dd>
                </dl>
        
