            <h3>Typografiske begreber</h3>
                <dl>
                    <dt>Minuskler</dt>
                    <dd>De ‘små’ bogstaver </dd>
                    <dt>X-height</dt>
                    <dd>Det er højden på minuskel ‘x’, målt fra bundlinien til toppen af bogstavet. Kan bl.a. benyttes til at parre typefaces.</dd>
                    <dt>Leading</dt>
                    <dd>Bedre kendt som ‘linieafstand’. Måles fra bundlinie til bundlinie. Typisk udregnes leading med 120-130% af skriftstørrelsen.</dd>
                    <dt>Kerning</dt>
                    <dd>Kendt som ‘knibning’. Afstanden mellem hvert enkelt bogstav kan justeres, således der skabes balance</dd>
                    <dt>Tracking</dt>
                    <dd>Justerer den overordnede afstand mellem alle bogstaver I et ord, eller en længere markering af ord.</dd>
                </dl>
            
